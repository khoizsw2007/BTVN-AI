"""## 🔨 BƯỚC 3 — Parse COCO annotation → Tạo tập classification

Dataset gốc là **keypoint-detection** → mỗi annotation có `category_id` (loại đường chỉ) và bounding box.  
Ta sẽ **crop từng vùng bbox** ra thành ảnh riêng, gán nhãn theo category → dùng train CNN classification.
"""

import json, shutil
import numpy as np
from PIL import Image
from pathlib import Path
from collections import defaultdict

OUTPUT_DIR = Path('/content/palm_classification')

def parse_coco_and_crop(split: str):
    """
    Đọc COCO annotation của split (train/valid/test),
    crop từng bbox ra, lưu vào OUTPUT_DIR/<split>/<class_name>/<img>.jpg
    """
    # Tìm file annotation JSON
    ann_file = DATASET_DIR / split / '_annotations.coco.json'
    img_dir  = DATASET_DIR / split
    if not ann_file.exists():
        print(f"  ⚠️  Không tìm thấy {ann_file}")
        return {}

    with open(ann_file) as f:
        coco = json.load(f)

    # Map id → tên class
    cat_map  = {c['id']: c['name'] for c in coco['categories']}
    # Map image_id → file_name
    img_map  = {i['id']: i['file_name'] for i in coco['images']}

    counts   = defaultdict(int)
    skipped  = 0

    for ann in coco['annotations']:
        img_id   = ann['image_id']
        cat_id   = ann['category_id']
        cat_name = cat_map.get(cat_id, f'class_{cat_id}')
        fname    = img_map.get(img_id)
        if fname is None:
            skipped += 1
            continue

        img_path = img_dir / fname
        if not img_path.exists():
            skipped += 1
            continue

        # COCO bbox: [x, y, w, h]
        bbox = ann.get('bbox')
        try:
            img = Image.open(img_path).convert('RGB')
            W, H = img.size

            if bbox and len(bbox) == 4 and bbox[2] > 10 and bbox[3] > 10:
                x, y, bw, bh = bbox
                # Thêm padding 10%
                pad = 0.10
                x1  = max(0,  int(x  - bw * pad))
                y1  = max(0,  int(y  - bh * pad))
                x2  = min(W,  int(x  + bw + bw * pad))
                y2  = min(H,  int(y  + bh + bh * pad))
                crop = img.crop((x1, y1, x2, y2))
            else:
                # Không có bbox hợp lệ → dùng cả ảnh
                crop = img

            # Lưu
            save_dir = OUTPUT_DIR / split / cat_name
            save_dir.mkdir(parents=True, exist_ok=True)
            out_name = f"{img_path.stem}_ann{ann['id']}.jpg"
            crop.save(save_dir / out_name, quality=92)
            counts[cat_name] += 1

        except Exception as e:
            skipped += 1

    print(f"  [{split}] Đã crop: {dict(counts)}  | Bỏ qua: {skipped}")
    return dict(counts)


