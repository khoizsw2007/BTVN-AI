    x        = keras_image.img_to_array(img_arr) / 255.0
    x        = np.expand_dims(x, axis=0)

    probs    = best_model.predict(x, verbose=0)[0]
    top_idxs = probs.argsort()[::-1][:top_k]

    results  = [{"class": CLASS_NAMES[i], "prob": probs[i]} for i in top_idxs]
    fortune  = get_fortune(results[0]['class'])
    return img, results, fortune


